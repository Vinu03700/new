public class FindLargest {

    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 8;

        if (num1 > num2) {
            System.out.println(num1 + " is the larger number.");
        } else {
            System.out.println(num2 + " is the larger number.");
        }
    }
}
